'''
Задача 6
Найти сумму цифр числа.
'''
print( '-------- 6 ---------' )
num = input( 'введите число: ')
lst = num.split('.')
lst[0] = int( lst[0] )
if len(lst) > 1:
    lst[1] = int(lst[1]) if len(lst[1]) > 0 else 0

sum = 0
for ni in lst:
    while ni > 0:
        sum += ni % 10
        ni = ni // 10

print( 'сумма цифр {} равна: {}'.format( num, sum ) )


