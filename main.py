'''
Задача 9
Найти максимальную цифру в числе
'''
print( '-------- 9 ---------' )
num = input( 'введите число: ')
nMax=0
for ch in num:
    if ch != '.':
        if int(ch) > nMax:
            nMax = int( ch )
print('Максимальная цифра {}: {}'.format(num,nMax))

